package rx;

/**
 * Наблюдатель реактивного потока.
 * Реализует паттерн Observer — получает элементы, ошибки и сигнал завершения.
 *
 * @param <T> тип элементов потока
 */
public interface Observer<T> {

    /**
     * Вызывается при поступлении очередного элемента.
     *
     * @param item элемент потока
     */
    void onNext(T item);

    /**
     * Вызывается при возникновении ошибки. После этого поток считается завершённым.
     *
     * @param t ошибка
     */
    void onError(Throwable t);

    /**
     * Вызывается при нормальном завершении потока.
     * После этого вызова новых элементов не будет.
     */
    void onComplete();
}
