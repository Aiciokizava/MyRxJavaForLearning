package rx.scheduler;

/**
 * Интерфейс планировщика — абстракция над пулом потоков.
 * Определяет, в каком потоке будет выполняться задача.
 */
public interface Scheduler {

    /**
     * Выполняет задачу в потоке, управляемом данным планировщиком.
     *
     * @param task задача для выполнения
     */
    void execute(Runnable task);

    /**
     * Освобождает ресурсы планировщика (останавливает пул потоков).
     */
    void shutdown();
}
